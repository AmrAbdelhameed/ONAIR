    @SuppressWarnings("StatementWithEmptyBody")
    @Override
    public boolean onNavigationItemSelected(MenuItem item) {
        // Handle navigation view item clicks here.
        int id = item.getItemId();

        if (id == R.id.nav_staff) {
            toolbar.setTitle("Staff");
            fragmentManager.beginTransaction().replace(R.id.content, new StaffFragment()).commit();
        } else if (id == R.id.nav_clients) {
            toolbar.setTitle("Clients");
            fragmentManager.beginTransaction().replace(R.id.content, new ClientsFragment()).commit();
        } else if (id == R.id.nav_groups) {
            toolbar.setTitle("Groups");
            fragmentManager.beginTransaction().replace(R.id.content, new GroupsFragment()).commit();
        } else if (id == R.id.nav_logout) {
            FirebaseAuth.getInstance().signOut();
            startActivity(new Intent(MainActivity.this, LoginActivity.class));
            finish();
        }

        DrawerLayout drawer = (DrawerLayout) findViewById(R.id.drawer_layout);
        drawer.closeDrawer(GravityCompat.START);
        return true;
    }
